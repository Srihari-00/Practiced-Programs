
public class Pattern7 
{

	public static void main(String[] args) 
	{
		//for columns
		for(int i=1;i<=5;i++)
		{
			//To Displaying the Spaces in Rows
			for(int j=1;j<=5;j++)
			{
				System.out.print(" ");	
			}
			
			//To Displaying the Rows
			for(int j=1;j<=5;j++)
			{ 
				System.out.print("*");
			}
			System.out.println();
		}
		
	}

}
