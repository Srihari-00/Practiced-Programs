package com.kodnest.creatingmutablestrings;

public class MutableStringBuffer {

	public static void main(String[] args) 
	{
		StringBuffer s1 = new StringBuffer();
		s1.append("Java ");//Assigning java
		System.out.println(s1);
		s1.append("SQL ");//concatinating with SQL
		System.out.println(s1);
		s1.append("Python ");//Concatinating with Python
		System.out.println(s1);
		s1.append("Apti");//Concatinating with Apti
		System.out.println(s1);//displaying

	}

}
