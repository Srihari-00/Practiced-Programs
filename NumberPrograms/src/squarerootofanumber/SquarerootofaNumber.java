package squarerootofanumber;

public class SquarerootofaNumber 
{
	void calculateSquareRootofaNumber(int x)//passing the parameters
	{
		//using the math function calculating the square roots
		int result=(int)Math.sqrt(x);
		//displaying the result
		System.out.println("The square root of a number is "+result);
	}

}
