
public class ProblemDecDo {

	public static void main(String[] args) 
	{
		//Declaring the variable I and assigning the value to it
		int i = 60;
		//using the Do While Loop
		do
		{
			//Printing the value of the I
			System.out.println(i);
			//Decrementing the value of I
			i--;
		}
		//Checking the condition whether the number is 
		//greater than or equal to 25 or not
		while(i>=25);

	}

}
