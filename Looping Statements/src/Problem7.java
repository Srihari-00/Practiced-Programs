
public class Problem7 {

	public static void main(String[] args) 
	{
		//Declaring a variable and assigning the value to it
		int i=1;
		//Using the do while loop
		do
		{
			//Checking the condition whether the value is equal to 7 or not
			if(i==7)
			{
				//If the condition is TRUE then break the loop 
				break;
			}
			//Printing the value of I
			System.out.println(i);
			//Incrementing the value of I
			i++;
		}
		//Checking the condition whether the number is less than or equal to 10
		while(i<=10);

	}

}
