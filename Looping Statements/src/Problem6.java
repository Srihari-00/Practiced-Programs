
public class Problem6 {

	public static void main(String[] args)
	{
		//Declaring the variable and initiallising the value to it.
		int i=1;
		//Using the while loop and checking whether the number is less than or equal to 10 or not
		while(i<=10)
		{
			//Checking whether the number is equal to 7 or not
			if(i==7)
			{
				//if the condition is TRUE, then break the loop
				break;
			}
			//Printing the value of I
			System.out.println(i);
			//Incrementing the value of I
			i++;
		}

	}

}
