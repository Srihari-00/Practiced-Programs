
public class Problem8 
{

	public static void main(String[] args) 
	{
		//Declaring the variable I and assigning the value to it
		//Checking the condition whether the number is less than or equal to 10 or not
		//Incrementing the value of I
		for(int i=1;i<=10;i++)
		{
			//Checking whether the number is equal to 6 or not
			if(i==6)
			{
				//If it is true then continue the loop
				continue;			
			
			}
			//Printing the value of the I
			System.out.println(i);

			
		}

	}

}
