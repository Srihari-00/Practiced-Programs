
public class Problem5 
{

	public static void main(String[] args) 
	{
		//using the for loop
		//Declaring the variable and intialising the value to it.
		//Checking whether the number is less than or equal to 10 or not.
		//Incrementing the value of the I.
		for(int i=1;i<=10;i++)
		{
			//Checking the number is equal to 7 or not
			if(i==7)
			{
				//if the condition is TRUE, break the loop
				break;
			}
			//Printing the value of the I
			System.out.println(i);
		}

	}

}
